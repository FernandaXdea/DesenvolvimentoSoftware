#For trabalha com sequencias ao inces de condições, simplifica alguns usos do while
# for<elemento> in <sequencia>:
#    <instrução1>

for letra in "texto":
    print(letra)

for numero in range(11):
    print(numero)

for numero in range(5,11): #Intervalo entre os numeros
    print(numero)

for numero in range(0,20,2): #Lista de 0 a 20 de 2 em 2
    print(numero)